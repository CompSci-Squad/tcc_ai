#!/usr/bin/env python3
"""Phase C locked evaluation — VAL-frozen recession-cluster assignment.

This script re-runs the Phase C evaluation under the locked protocol:
  - Recession cluster is selected on VAL (not TEST) to eliminate test-set leakage.
  - Best downstream cell is also selected on VAL.
  - C1 multi-label evaluation uses the VAL-derived assignment.

Requires embeddings (Z_train/Z_val/Z_test.parquet) to be pre-computed.
For iTransformer: results/sm_outputs/itransformer-1777581449-0d38/embeddings/
For Phase E:      results/phase_e/{enc}/embeddings/
For baselines:    results/phase_c_comparison/{enc}/emb/  (generated by run_phase_c_comparison.py)

Usage:
    cd tcc_ai
    source ../tcc.env
    uv run python scripts/run_phase_c_locked.py

Output:
    outputs/tables/phase_c_locked_all_cells.csv
    outputs/tables/phase_c_locked_best_cell.csv
    outputs/tables/phase_c_canonical_pca_kmeans.csv
    results/clustering_ablation/W6_d7_K4_b1/val_{cell}.parquet  (VAL label cache)
    results/phase_e/{enc}/ablation/val_{cell}.parquet
    results/phase_c_comparison/{enc}/val_{cell}.parquet
"""

